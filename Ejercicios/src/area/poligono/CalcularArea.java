package area.poligono;

public class CalcularArea {
    public static double calcularArea(Poligono poli) {
        return poli.calcularArea();
    }

    public static void main(String[] args) {
        Poligono triangulo = new Triangulo(3, 4);
        Poligono rectangulo = new Rectangulo(5, 6);
        Poligono cuadrado = new Cuadrado(4);

        System.out.println("Area triangulo: " + calcularArea(triangulo));
        System.out.println("Area rectangulo: " + calcularArea(rectangulo));
        System.out.println("Area cuadrado: " + calcularArea(cuadrado));
    }
}